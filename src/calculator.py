# calculator.py

def add(a, b):
    """
    Эта функция принимает два числа и возвращает их сумму.
    """
    return a + b

def divide(a, b):
    """
    Эта функция возвращает результат деления одного числа на другое.
    """
    if b == 0:
        raise ValueError("Нельзя делить на ноль")
    return a / b